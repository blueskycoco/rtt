/*****************************************************************************************
函数名称:    void  GUI_PutString35(uint8 fontType, INT16U x, INT16U y, char *str)
版本号:        1.0.0
生成日期:    2008.08.31
作者:        影舞者
功能说明:    输出显示字符串((8*8字体，没有自动换行功能)
输入参数:    x        指定显示位置，x坐标
            y        指定显示位置，y坐标
            str        要显示的ASCII码字符串
输出参数:    无
其它说明:    操作失败原因是指定地址超出有效范围
*****************************************************************************************/
void  GUI_PutString35(uint8 fontType, INT16U x, INT16U y, char *str, uint16 color, uint16 bkcolor)
{    
    INT08U    Ch, GUI_HZKFlag = 0;
    INT16U    x0 = x, GUI_GBCode = 0;
    
    while (1)
    { 
        if ((x > BOARD_LCD_WIDTH) || (y > BOARD_LCD_HEIGHT))
            break;

        Ch = *str;

        if (Ch == '\0')
            break;

        if (Ch == '\r')
            continue;

        if (Ch == '\n')
        {
            x = x0;
            y = y + 35;
            continue;
        }

        if (Ch >= 0x80)
        {
            if (GUI_HZKFlag == 0)
            {
                GUI_HZKFlag = 1;
                GUI_GBCode  = Ch & 0xFF;
            }
            else
            {
                GUI_HZKFlag = 0;
                GUI_GBCode  = GUI_GBCode | ((Ch << 8) & 0xFF00);

                GUI_PutHzk35(fontType, x, y, GUI_BinarySearch35(GUI_GBCode), color, bkcolor);
                
                x = x + 35;
            }

            str++;
            continue;
        }
        
        GUI_HZKFlag = 0;
        
        GUI_PutChar35(fontType, x, y, *str++, color, bkcolor);
        x = x + 18;                                                                // 下一个字符显示位置，y不变(即不换行)
    }
}





