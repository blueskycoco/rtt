/*****************************************************************************************
函数名称:    void  GUI_PutChar64(uint8 fontType, INT16U x, INT16U y, INT08U ch)
版本号:        1.0.0
生成日期:    2008.08.31
作者:        影舞者
功能说明:    显示ASCII码(8*8字体)，显示值为20H-7FH(若为其它值，则显示' ')
输入参数:    x        指定显示位置，x坐标
            y        指定显示位置，y坐标
            ch        要显示的ASCII码值
输出参数:    无
其它说明:    操作失败原因是指定地址超出有效范围
*****************************************************************************************/
void  GUI_PutChar64(uint8 fontType, INT16U x, INT16U y, char ch, uint16 color, uint16 bkcolor)
{
    INT08U   font_dat;
    INT16U   i, k;
    const unsigned char * pixelinfo;

    if (((x+32) > BOARD_LCD_WIDTH) || ((y+63) > BOARD_LCD_HEIGHT))
        return;

    if ((ch >= '0') && (ch <= '9'))
    {
        TFT_SetWindowEXT(x, x+32, y, y+63);                     //字宽*高为：8*16
        pixelinfo = &FONT33_64ASCII[(ch - '0') * 320];
    }
    else if (ch == ' ')
    {
        TFT_SetWindowEXT(x, x+32, y, y+63);                     //字宽*高为：8*16
        pixelinfo = &FONT33_64ASCII[10 * 320];
    }
    else if (ch == '.')
    {
        TFT_SetWindowEXT(x, x+32, y, y+63);                     //字宽*高为：8*16
        pixelinfo = &FONT33_64ASCII[11 * 320];
    }
    else if (ch == '-')
    {
        TFT_SetWindowEXT(x, x+32, y, y+63);                     //字宽*高为：8*16
        pixelinfo = &FONT33_64ASCII[12 * 320];
    }
    else
    {
        return;
    }

    for (i = 0; i < 64; i++, y++)
    {
        font_dat = pixelinfo[i * 5 + 0];                        // 读取点阵数据

        for (k = 0; k < 8; k++, x++)
        { 
            if (font_dat & DCB2HEX_TAB[k])                         
                TFT_WriteDataEXT(color); 
            else  
                TFT_WriteDataEXT(bkcolor); 
        }
        
        font_dat = pixelinfo[i * 5 + 1];                        // 读取点阵数据

        for (k = 0; k < 8; k++, x++)
        { 
            if (font_dat & DCB2HEX_TAB[k])                         
                TFT_WriteDataEXT(color); 
            else  
                TFT_WriteDataEXT(bkcolor); 
        }

        font_dat = pixelinfo[i * 5 + 2];                        // 读取点阵数据

        for (k = 0; k < 8; k++, x++)
        { 
            if (font_dat & DCB2HEX_TAB[k])                         
                TFT_WriteDataEXT(color); 
            else  
                TFT_WriteDataEXT(bkcolor); 
        }

        font_dat = pixelinfo[i * 5 + 3];                        // 读取点阵数据

        for (k = 0; k < 8; k++, x++)
        { 
            if (font_dat & DCB2HEX_TAB[k])                         
                TFT_WriteDataEXT(color); 
            else  
                TFT_WriteDataEXT(bkcolor); 
        }

        font_dat = pixelinfo[i * 5 + 4];                        // 读取点阵数据

        for (k = 0; k < 1; k++, x++)
        { 
            if (font_dat & DCB2HEX_TAB[k])                         
                TFT_WriteDataEXT(color); 
            else  
                TFT_WriteDataEXT(bkcolor); 
        }
    }
}




/*****************************************************************************************
函数名称:    void  GUI_PutString35(uint8 fontType, INT16U x, INT16U y, char *str)
版本号:        1.0.0
生成日期:    2008.08.31
作者:        影舞者
功能说明:    输出显示字符串((8*8字体，没有自动换行功能)
输入参数:    x        指定显示位置，x坐标
            y        指定显示位置，y坐标
            str        要显示的ASCII码字符串
输出参数:    无
其它说明:    操作失败原因是指定地址超出有效范围
*****************************************************************************************/
void  GUI_PutString64(uint8 fontType, INT16U x, INT16U y, char *str, uint16 color, uint16 bkcolor)
{    
    INT08U    Ch, GUI_HZKFlag = 0;
    INT16U    x0 = x, GUI_GBCode = 0;
    
    while (1)
    { 
        if ((x > BOARD_LCD_WIDTH) || (y > BOARD_LCD_HEIGHT))
            break;

        Ch = *str;

        if (Ch == '\0')
            break;

        if (Ch == '\r')
            continue;

        if (Ch == '\n')
        {
            x = x0;
            y = y + 64;
            continue;
        }

        if (Ch >= 0x80)
        {
            if (GUI_HZKFlag == 0)
            {
                GUI_HZKFlag = 1;
                GUI_GBCode  = Ch & 0xFF;
            }
            else
            {
                GUI_HZKFlag = 0;
                GUI_GBCode  = GUI_GBCode | ((Ch << 8) & 0xFF00);

                //GUI_PutHzk64(fontType, x, y, GUI_BinarySearch35(GUI_GBCode), color, bkcolor);
                
                x = x + 64;
            }

            str++;
            continue;
        }
        
        GUI_HZKFlag = 0;
        
        GUI_PutChar64(fontType, x, y, *str++, color, bkcolor);
        x = x + 33;                                                                // 下一个字符显示位置，y不变(即不换行)
    }
}





