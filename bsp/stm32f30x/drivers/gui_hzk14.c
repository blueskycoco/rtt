/*****************************************************************************************
函数名称:    const unsigned char * GUI_Search14(INT16U gb)
版本号:        1.0.0
生成日期:    2008.08.31
作者:        影舞者
功能说明:    查找有没有汉字编码
输入参数:    gb        机内编码
输出参数:    点阵编码
其它说明:    采用二分查找法
*****************************************************************************************/
const unsigned char * GUI_Search14(INT16U gb)
{  
    INT16U    num, left, right, middle;

    num   = GUI_sizeof(FONT7_14HZK) / GUI_sizeof(HZK7_14);
    left  = 0; 
    right = num - 1; 

    while (left <= right)
    {  
        middle = (left + right) / 2; 

        if (gb == FONT7_14HZK[middle].code)
            return FONT7_14HZK[middle].code_data; 

        if (gb > FONT7_14HZK[middle].code) 
            left  = middle + 1; 
        else 
            right = middle - 1; 
    }  

    return NULL; 
}




/*****************************************************************************************
函数名称:    void  GUI_PutChar14(uint8 fontType, INT16U x, INT16U y, INT08U ch)
版本号:        1.0.0
生成日期:    2008.08.31
作者:        影舞者
功能说明:    显示ASCII码(8*8字体)，显示值为20H-7FH(若为其它值，则显示' ')
输入参数:    x        指定显示位置，x坐标
            y        指定显示位置，y坐标
            ch        要显示的ASCII码值
输出参数:    返回值为1时表示操作成功，为0时表示操作失败
其它说明:    操作失败原因是指定地址超出有效范围
*****************************************************************************************/
void  GUI_PutChar14(uint8 fontType, INT16U x, INT16U y, char ch, uint16 color, uint16 bkcolor)
{
    INT08U   font_dat;
    INT16U   i, k, offset;

    if (((x+6) > BOARD_LCD_WIDTH) || ((y+13) > BOARD_LCD_HEIGHT))
        return;

    if ((ch < 0x20) || (ch > 0x7f))
        return;

    TFT_SetWindowEXT(x, x+6, y, y+13);                     //字宽*高为：8*16
    offset = (ch - 0x20) * 14;
    
    for (i = 0; i < 14; i++, y++)
    {
        font_dat = FONT7_14ASCII[offset + i];                        // 读取点阵数据

        for (k = 0; k < 7; k++, x++)
        { 
            if (font_dat & DCB2HEX_TAB[k])                             // 设置相应的点
                TFT_WriteDataEXT(color); 
            else  
                TFT_WriteDataEXT(bkcolor); 
        }
    }
}




